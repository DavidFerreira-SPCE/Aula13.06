// 12 Crie uma função que receba um texto e retorne o mesmo texto em letras maiúsculas.

function Texto(Nome) {
      let exemplo = Nome
      let maiúsculas = exemplo.toUpperCase()
      //É uma função do JavaScript para colocar caixa alta
      return maiúsculas;
}
console.log("Mostrando a palavra em caixa alta: " + Texto("brilho eterno de uma mente sem lembranças"))  